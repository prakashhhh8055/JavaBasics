package Basics;

public class forloop {

	public static void main(String[] args) {
		// print 1 to 10 number but each number should should be checked whether the number is even or odd number.
		for(int i=1;i<=10;i++)
		{
			if(i%2==0)
			{
				System.out.println(i+" is Even Number");
			}
			else
			{
				System.out.println(i+" is Odd Number");
			}
		}
			

	}

}
